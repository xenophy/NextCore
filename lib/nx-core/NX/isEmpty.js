// {{{ NX.isEmpty

module.exports = function(value, allowEmptyString) {
    return (
        (value === null) ||
        (value === undefined) ||
        (!allowEmptyString ? value === '' : false) ||
        (NX.isArray(value) && value.length === 0)
    );
};

