module.exports = function(len) {

    var buf = [],
        ret = [];
        chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789',
        charlen = chars.length;

    for(var i = 0; i < len; ++i) {
        buf.push(chars[NX.getRandomInt(0, charlen - 1)]);
    }

    return buf.join('');
};

